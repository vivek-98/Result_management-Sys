    private void table_update(){
        int c;
         String name=txtname.getText();
         String rollno=txtrollno.getText();
         String marks=tmarks.getText();
          try{
              Connection conn=DriverManager.getConnection("jdbc:sqlite:E:\\databases.db");
              PreparedStatement insert= conn.prepareStatement("select * from Blue");
              ResultSet rs=insert.executeQuery();
              ResultSetMetaData rss=rs.getMetaData();
              c =rss.getColumnCount();
              
              DefaultTableModel def=(DefaultTableModel)jTable2.getModel();
              def.setRowCount(0);
              
              while(rs.next())
              {
                  Vector v=new Vector();
                  
                  for(int a=1; a<=c; a++)
                  {
                      v.add(rs.getString("id"));
                      v.add(rs.getString("name"));
                      v.add(rs.getString("rollno"));
                      v.add(rs.getString("totalmarks"));
                  }
                  def.addRow(v);
              }
              
              
              
              
              
              txtname.setText("");
              txtrollno.setText("");
              tmarks.setText("");
              txtname.requestFocus();
              
              
          } catch (SQLException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel def=(DefaultTableModel)jTable2.getModel();
        int select=jTable2.getSelectedRow();
        
         try{
             
             int id= Integer.parseInt(def.getValueAt(select,0).toString());
             String name=txtname.getText();
             String rollno=txtrollno.getText();
             String marks=tmarks.getText();
             
             
             
              Connection conn=DriverManager.getConnection("jdbc:sqlite:E:\\databases.db");
              PreparedStatement insert= conn.prepareStatement("update Blue set name=?, rollno=?, totalmarks=? where id=?");
              insert.setString(1, name);
              insert.setString(2, rollno);
              insert.setString(3, marks);
              insert.setInt(4, id); 
              insert.executeUpdate();
              
              JOptionPane.showMessageDialog(this, "Record Updated");
              table_update();
              
              txtname.setText("");
              txtrollno.setText("");
              tmarks.setText("");
              txtname.requestFocus();
              
              
          } catch (SQLException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

